package query

import "github.com/anatoly-tenenev/spec-cli/internal/application/help/helpmodel"

func HelpSpec() helpmodel.CommandSpec {
	return helpmodel.CommandSpec{
		Name:    "query",
		Summary: "read many entities with structural filters and deterministic pagination",
		Syntaxes: []string{
			"spec-cli query [options]",
		},
		Options: []helpmodel.OptionSpec{
			{
				Name:             "--type",
				ValueSyntax:      "<entity_type>",
				TakesValue:       true,
				Required:         false,
				Repeatable:       true,
				SchemaDerived:    true,
				SchemaDerivation: "entity type keys from Schema.entity",
				Description:      "Early entity type filter.",
			},
			{
				Name:             "--where",
				ValueSyntax:      "<expr>",
				TakesValue:       true,
				Required:         false,
				Repeatable:       false,
				SchemaDerived:    true,
				SchemaDerivation: "JMESPath expression over query item schema",
				Description:      "Schema-aware JMESPath filter expression.",
			},
			{
				Name:             "--select",
				ValueSyntax:      "<field>",
				TakesValue:       true,
				Required:         false,
				Repeatable:       true,
				SchemaDerived:    true,
				SchemaDerivation: "selectors from CLI projection-namespace",
				Description:      "Projection-namespace selector.",
			},
			{
				Name:             "--sort",
				ValueSyntax:      "<field[:asc|desc]>",
				TakesValue:       true,
				Required:         false,
				Repeatable:       true,
				SchemaDerived:    true,
				SchemaDerivation: "sort fields from CLI filter-namespace",
				Description:      "Deterministic sort term.",
			},
			{
				Name:             "--limit",
				ValueSyntax:      "<n>",
				TakesValue:       true,
				Required:         false,
				Repeatable:       false,
				SchemaDerived:    false,
				SchemaDerivation: "none",
				Description:      "Page size; integer >= 0; default 100.",
			},
			{
				Name:             "--offset",
				ValueSyntax:      "<n>",
				TakesValue:       true,
				Required:         false,
				Repeatable:       false,
				SchemaDerived:    false,
				SchemaDerivation: "none",
				Description:      "Page offset; integer >= 0; default 0.",
			},
		},
		Rules: []string{
			"projection-namespace argument is --select.",
			"filter-namespace arguments are --sort and --where.",
			"projection-namespace selectors: type, id, slug, revision, createdDate, updatedDate, meta.<name>, refs, refs.<field>, content.raw, content.sections, content.sections.<name>.",
			"filter-namespace sort paths: type, id, slug, revision, createdDate, updatedDate, meta.<name>, refs.<field>.resolved, refs.<field>.type, refs.<field>.id, refs.<field>.slug, refs.<field>.reason, content.raw, content.sections.<name>.",
			"--type uses schema-derived entity type keys and is not part of projection-namespace or filter-namespace.",
			"If --select is omitted, default projection is type, id, slug, meta and refs.",
			"Query guarantees deterministic ordering.",
			"If --sort is omitted, effective sort is type:asc, then id:asc.",
			"If --sort is provided, effective sort is user sort plus hidden tail type:asc, then id:asc, unless that tail is already present at the end.",
			"--where uses JMESPath truthiness over schema-aware query-item context.",
			"--where forbids content root access except content.sections... and forbids content.raw.",
			"--where forbids meta.<field> access for entityRef fields; use refs.<field>.",
		},
		Examples: []string{
			"spec-cli query --type feature --select id --select meta.status",
			"spec-cli query --where \"meta.status == 'active'\"",
			"spec-cli query --where \"refs.owner.id == 'SVC-2'\"",
			"spec-cli query --where \"contains(content.sections.summary, 'retry')\"",
			"spec-cli query --where \"length(refs.watchers[?reason == 'missing']) > `0`\"",
			"spec-cli query --sort updatedDate:desc --limit 50 --offset 0",
		},
	}
}
